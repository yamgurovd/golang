// Требуется написать программу, при выполнении которой с клавиатуры считываются два натуральных числа
// A и B (каждое не более 100, A < B). Вывести сумму всех чисел от A до B  включительно.
package main

import "fmt"

func main() {
	var A, B int

	// Считываем два натуральных числа A и B
	fmt.Print("Введите число A (A < B и A, B ≤ 100): ")
	fmt.Scan(&A)
	fmt.Print("Введите число B: ")
	fmt.Scan(&B)

	// Проверяем условия: A < B и оба числа не более 100
	if A < 1 || A > 100 || B < 1 || B > 100 || A >= B {
		fmt.Println("Ошибка: Убедитесь, что 1 ≤ A < B ≤ 100.")
		return
	}

	// Вычисляем сумму всех чисел от A до B включительно
	sum := 0
	for i := A; i <= B; i++ {
		sum += i
	}

	// Выводим результат
	fmt.Printf("Сумма чисел от %d до %d включительно: %d\n", A, B, sum)
}
